    private void cmdRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdRetirarActionPerformed
        double egreso;
        int sw = 0;
        do {
            try {
                egreso = Double.parseDouble(JOptionPane.showInputDialog(this, "Digite Cantidad a retirar"));
                if (c.getSaldo_actual() < egreso) {
                    JOptionPane.showMessageDialog(this, "La cantidad de dinero a retirar es mayor \n"
                        + "que la cantidad de dinero actual");
                    sw = 0;
                } else if (egreso < 0) {
                    JOptionPane.showMessageDialog(this, "digite un numero valido", "Error", JOptionPane.ERROR_MESSAGE);
                    sw = 0;
                } else {
                    c.retirar(egreso);
                    sw = 1;
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "digite un numero valido", "Error", JOptionPane.ERROR_MESSAGE);
                sw = 0;
            } catch (NullPointerException e) {
                int res = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas salir?", "Salir", JOptionPane.YES_NO_OPTION);
                if (res == 0) {
                    sw = 1;
                } else {
                    sw = 0;
                }
            }
        } while (sw == 0);

    }//GEN-LAST:event_cmdRetirarActionPerformed

